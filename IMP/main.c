{
	/* reset timer */
	PIT->CHANNEL[0].TCTRL &= ~PIT_TCTRL_TEN_MASK;
	PIT->CHANNEL[0].TCTRL |= PIT_TCTRL_TEN_MASK;

	Delay(100, 1000);
	if (PORTE->ISFR &= BUTTON_SW5)
	{
			pressedSW5 = 1;
	}
	else if (PORTE->ISFR &= BUTTON_SW3)
	{
		pressedSW3 = 1;
	}
	else if (PORTE->ISFR &= BUTTON_SW4)
	{
			pressedSW4 = 1;
	}
	else if (PORTE->ISFR &= BUTTON_SW2)
	{
			pressedSW2 = 1;
	}
	else;
}

/* Handler of the timer interruptions */
void PIT0_IRQHandler(void)
{

	if(PIT->CHANNEL[0].TFLG & PIT_TFLG_TIF_MASK)
	{
		PIT->CHANNEL[0].TFLG &= PIT_TFLG_TIF_MASK;
		NVIC_ClearPendingIRQ(PIT0_IRQn);
	}
	timer = 1;
}


/* Conversion of requested column number into the 4-to-16 decoder control.  */
void column_select(unsigned int col_num)
{
	unsigned i, result, col_sel[4];

	for (i =0; i<4; i++) {
		result = col_num / 2;	  // Whole-number division of the input number
		col_sel[i] = col_num % 2;
		col_num = result;

		switch(i) {

			// Selection signal A0
		    case 0:
				((col_sel[i]) == 0) ? (PTA->PDOR &= ~GPIO_PDOR_PDO( GPIO_PIN(8))) : (PTA->PDOR |= GPIO_PDOR_PDO( GPIO_PIN(8)));
				break;

			// Selection signal A1
			case 1:
				((col_sel[i]) == 0) ? (PTA->PDOR &= ~GPIO_PDOR_PDO( GPIO_PIN(10))) : (PTA->PDOR |= GPIO_PDOR_PDO( GPIO_PIN(10)));
				break;

			// Selection signal A2
			case 2:
				((col_sel[i]) == 0) ? (PTA->PDOR &= ~GPIO_PDOR_PDO( GPIO_PIN(6))) : (PTA->PDOR |= GPIO_PDOR_PDO( GPIO_PIN(6)));
				break;

			// Selection signal A3
			case 3:
				((col_sel[i]) == 0) ? (PTA->PDOR &= ~GPIO_PDOR_PDO( GPIO_PIN(11))) : (PTA->PDOR |= GPIO_PDOR_PDO( GPIO_PIN(11)));
				break;

			// Otherwise nothing to do...
			default:
				break;
		}
	}
}

/* Function writes from columns_array 16 values (columns) into the display*/
void Write16Columns(int cur_index)
{
	int calculated_index = 0;
	int overflow_position = 0;
	for (int k = 0; k < 16; k++)
	{
		calculated_index = cur_index + k;
		if (calculated_index >= index)
		{
			if (!overflow_position)
			{
				overflow_position = calculated_index;
			}
			calculated_index = calculated_index - overflow_position;
		}
		PTA->PDOR &= GPIO_PDOR_PDO(0x00000000);
		column_select(k);
		PTA->PDOR |= GPIO_PDOR_PDO(columns_array[calculated_index]);
		PTE->PDDR &= ~GPIO_PDDR_PDD( GPIO_PIN(28) );
		Delay(tdelay3, tdelay3);
		PTE->PDOR |= GPIO_PDOR_PDO( GPIO_PIN(28));
	}
}

